import { Injectable, ExecutionContext } from '@nestjs/common';
import { Reflector } from '@nestjs/core';
import { AuthGuard } from '@nestjs/passport';
import { IS_PUBLIC_KEY } from '../decorators/public.decorator';

/**
 * Try user/driver JWT first, then admin. If both access secrets match the admin
 * secret, a customer token could otherwise verify as admin-jwt and fail with
 * "Admin account not found or inactive".
 */
@Injectable()
export class JwtAuthGuard extends AuthGuard(['jwt', 'admin-jwt']) {
  constructor(private reflector: Reflector) {
    super();
  }

  canActivate(context: ExecutionContext) {
    const isPublic = this.reflector.getAllAndOverride<boolean>(IS_PUBLIC_KEY, [
      context.getHandler(),
      context.getClass(),
    ]);
    if (isPublic) return true;
    return super.canActivate(context);
  }
}
